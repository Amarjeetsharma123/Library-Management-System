    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String user=jTextField1.getText();
        String pass=String.valueOf(jPasswordField1.getPassword());
        try
        {
            Connection con=ConnectToDb.connect();
            String q="select *from Login where Username=? and Password=?";
            PreparedStatement p=con.prepareStatement(q);
            p.setString(1, user);
            p.setString(2, pass);
            ResultSet rs=p.executeQuery();
            if(rs.next())
            {
                JOptionPane.showMessageDialog(null, "LoginSuccessFull");
                MainSystem m=new MainSystem();
                m.setVisible(true);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "user/pass inavlid");
            }
        }
        catch(HeadlessException |SQLException e)
        {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

