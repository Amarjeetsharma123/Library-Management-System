    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String user=jTextField1.getText().trim();
        String query="select *from Login where Username=?";
        try
        {
            Connection con=ConnectToDb.connect();
            PreparedStatement p=con.prepareStatement(query);
            p.setString(1, user);
            ResultSet rs=p.executeQuery();
            if(rs.next())
            {
                JOptionPane.showMessageDialog(null, "User Found","Success",1);
                Showmenu();
                String sq=rs.getString(4);
                jTextField2.setText(sq);
            }
            
            
            
         
            else
            {
                JOptionPane.showMessageDialog(null, "Username Not  Found");
                
            }
        }
        catch(SQLException e)
        {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String query= "select Id from Login where Username=?and SecurityQ=?and Answer=?"; 
        String user=jTextField1.getText();
        String Secu=jTextField2.getText();
        String ans=jTextField3.getText();
        try
        {
            Connection con=ConnectToDb.connect();
            PreparedStatement p=con.prepareStatement(query);
            p.setString(1, user);
            p.setString(2, Secu);
            p.setString(3, ans);
            ResultSet rs=p.executeQuery();
            if(rs.next())
            {
                String query1="update Login set  Password=? where Id=?";
                p=con.prepareStatement(query1);
                p.setString(1, jTextField4.getText());
                p.setInt(2, rs.getInt(1));
                int r=p.executeUpdate();
                if(r>0)
                {
                    
                  JOptionPane.showMessageDialog(null, "Password changed","Sucess",1);
                    setVisible(false);
                  new Loginform().setVisible(true);
                  
                }
            
            }
            
            else
            {
                JOptionPane.showMessageDialog(null, "Please write username or Answer");
            }
            
        }
        
        catch(SQLException e)
        {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

