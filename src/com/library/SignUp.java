    private void jTextField1FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField1FocusLost
        if(jTextField1.getText().trim().equalsIgnoreCase("Username")||
                jTextField1.getText().trim().equalsIgnoreCase(""))
        {
            jTextField1.setText("Username");
            jTextField1.setForeground(new Color(153,153,153));
        }
        try
        {
            Connection con=ConnectToDb.connect();
            String query="select *from Login where Username=?";
            PreparedStatement p=con.prepareStatement(query);
            p.setString(1, jTextField1.getText());
            ResultSet rs=p.executeQuery();
        
        if(rs.next())
        {
            JOptionPane.showMessageDialog(null, "Username Already Exit","Please Enter Another Username",2);
            jTextField1.setText("Username");
            jTextField1.setForeground(new Color(153,153,153));
        }
        }
        catch(SQLException e)
                {
                JOptionPane.showMessageDialog(null, e);
                
                }
    }//GEN-LAST:event_jTextField1FocusLost

