    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String user=jTextField1.getText();
       String pass=jTextField2.getText();
       String sec=String.valueOf(jComboBox1.getSelectedItem());
       String ans=jTextField4.getText();
       try
       {
           Connection con= ConnectToDb.connect();
           String query="insert into login(Username,Password,SecurityQ,Answer) values(?,?,?,?)";
           PreparedStatement p=con.prepareStatement(query);
           p.setString(1, user);
           p.setString(2, pass);
           p.setString(3, sec);
           p.setString(4, ans);
           int r=p.executeUpdate();
           
           if(r==0)
           {
               JOptionPane.showMessageDialog(null, "Record Not  Saved","Please check your query",3);
           }
           else
           {
                JOptionPane.showMessageDialog(null, "Record  Saved  ","Congrats",1);
                new Loginform().setVisible(true);
               
           }
           
       }
       catch(HeadlessException |SQLException e)
       {
            JOptionPane.showMessageDialog(null, "Error");
       }
    }//GEN-LAST:event_jButton1ActionPerformed

